#include<stdio.h>
int main(){
	int zi,mu,r,zi_save,mu_save;
	scanf("%d/%d",&zi,&mu);
	zi_save=zi;
	mu_save=mu;
	if(zi==mu) printf("1/1");
	else{
		r=zi%mu;
		for(;r!=0;r=zi%mu){
			zi=mu;
			mu=r;
		}
		printf("%d/%d",zi_save/mu,mu_save/mu);
	}
	return 0;
}
