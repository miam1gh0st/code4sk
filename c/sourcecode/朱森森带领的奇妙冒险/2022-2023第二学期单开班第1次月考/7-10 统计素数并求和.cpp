#include<stdio.h>
int main(){
	int m,n,i,j,cnt=0,sum=0,flag=1;
	scanf("%d %d",&m,&n);
	if(m==1) m=2;
	for(i=m;i<=n;i++){
		flag=1;
		for(j=2;j<i-1;j++){
			if(i%j==0){
				flag=0;
				break;	
			}
		}
		if(flag){
			cnt++;
			sum+=i;
		}
	}
	printf("%d %d\n",cnt,sum);
	return 0;
}
