#include<stdio.h>
int main()
{
    int n,i,j,sum=0;
    scanf("%d",&n);
    int A[100][100];
    for(i=0;i<n;i++)
    {
        for(j=0;j<n;j++)
        {
            scanf("%d",&A[i][j]);
        }
    }
    for(i=0;i<n;i++)
    {
        for(j=0;j<n;j++)
        {
            if(!((i==i&j==n-i-1)||i==n-1||j==n-1))
            {
                sum+=A[i][j];
            }
        }
    }
    printf("%d",sum);
    return 0;
}
