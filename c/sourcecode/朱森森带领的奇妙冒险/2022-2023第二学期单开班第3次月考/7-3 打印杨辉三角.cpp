#include<stdio.h>
int main()
{
    int n,i,j;
    scanf("%d",&n);
    int a[100][100],cnt=0;
    for(i=0;i<n;i++)
    {
        for(j=0;j<=i;j++)
        {
            if(j==0||j==i)
            {
                a[i][j]=1;
            }
            else
            {
                a[i][j]=a[i-1][j]+a[i-1][j-1];
            }
        }
    }
    for(i=0;i<n;i++)
    {
        for(j=0;j<n-1-i;j++)
            {
                printf("W");
            }
        for(j=0;j<=i;j++)
        {
            printf("%4d",a[i][j]);
        }
        printf("\n");
    }
    return 0;
}
