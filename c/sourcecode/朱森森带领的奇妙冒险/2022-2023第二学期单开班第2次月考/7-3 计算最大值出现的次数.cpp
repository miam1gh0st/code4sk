#include<stdio.h>
int main(){
	int n,i;
	scanf("%d",&n);
	int a[n];
	for(i=0;i<n;i++){
		scanf("%d",&a[i]);
	}
	int max=a[0];
	for(i=0;i<n;i++){
		if(a[i]>max) max=a[i];
	}
	int cnt=0;
	for(i=0;i<n;i++){
		if(max==a[i]) cnt++;
	}
	printf("%d %d\n",max,cnt);
	return 0;
}
