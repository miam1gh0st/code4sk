#include<stdio.h>
int main(){
	int n;
	int b[10000];
	while(scanf("%d",&n)==1){
		int i,cnt=0;
		if(n==0) printf("0");
		for(i=0;n!=0;n/=2){
			b[i]=n%2;
			cnt++;
			i++;
		}
		for(i=cnt-1;i>=0;i--){
			printf("%d",b[i]);
		}
		printf("\n");
	}
	return 0;
}


