#include <stdio.h>
#define MAXN 10

int FindArrayMax( int a[], int n );

int main()
{
    int i, n;
    int a[MAXN];
    
    scanf("%d", &n);
    for( i=0; i<n; i++ ){
        scanf("%d", &a[i]);
    }

    printf("%d\n", FindArrayMax(a, n));
   
    return 0;
}

int FindArrayMax( int a[], int n ){
	int maxnum=a[0];
	for(int j=0;j<n;j++){
		if(a[j]>maxnum) maxnum=a[j];
	}
	return maxnum;
}


