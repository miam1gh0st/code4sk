#include<stdio.h>
int main(){
	int N,i;
	scanf("%d",&N);
	int a[N];
	for(i=0;i<N;i++){
		scanf("%d",&a[i]);
	}
	int max=a[0],max_i=0;
	for(i=0;i<N;i++){
		if(max<a[i]){
			max=a[i];
			max_i=i;
		};
	}
	int t=a[N-1];
	a[N-1]=max;
	a[max_i]=t;
	
	int min=a[0],min_i=0;
	for(i=0;i<N;i++){
		if(min>a[i]){
			min=a[i];
			min_i=i;
		};
	}
	t=a[0];
	a[0]=min;
	a[min_i]=t;
	for(i=0;i<N;i++){
		printf("%d ",a[i]);
	}
	return 0;
}

